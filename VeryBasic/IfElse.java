package VeryBasic;

public class IfElse {
    public static void main(String[] args) {
        int n=8;
        int m=7;
        if(n!=m)                 // here can not be non zero value like c only
                              // boolean value.
        {
            System.out.println("Not Equal");
        }
        else {
            System.out.println("Same");
        }
    }
}
